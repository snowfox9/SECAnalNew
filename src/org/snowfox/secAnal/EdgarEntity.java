package org.snowfox.secAnal;

public class EdgarEntity {

    public String Seq;
    public String Description;
    public String Document;
    public String Type;
    public String Size;

    public EdgarEntity(String Seq, String Description, String Document, String Type, String Size)
    {
        this.Seq = Seq;
        this.Description = Description;
        this.Document = Document;
        this.Type = Type;
        this.Size = Size;
    }

}
